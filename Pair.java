package sample;

public class Pair
{
  public Integer a;
  public Integer b;

  public Pair(Integer a, Integer b)
  {
    this.a = a;
    this.b = b;
  }
  //***********************************
  //Returns the integer associated with
  //the a side of the pair
  //***********************************
  public Integer getA()
  {
    return a;
  }
  //***********************************
  //Returns the integer associated with
  //the b side of the pair
  //***********************************
  public Integer getB()
  {
    return b;
  }

}
